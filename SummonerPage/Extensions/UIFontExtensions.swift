import UIKit

extension UIFont {
    class var systemFont10: UIFont {
        return .systemFont(ofSize: 10)
    }
    
    class var systemFont10Bold: UIFont {
        return .systemFont(ofSize: 10, weight: .bold)
    }
    
    class var systemFont12: UIFont {
        return .systemFont(ofSize: 12)
    }
    
    class var systemFont14: UIFont {
        return .systemFont(ofSize: 14)
    }
    
    class var systemFont14Bold: UIFont {
        return .systemFont(ofSize: 14, weight: .bold)
    }
    
    class var systemFont16: UIFont {
        return .systemFont(ofSize: 16)
    }
    
    class var systemFont16Bold: UIFont {
        return .systemFont(ofSize: 16, weight: .bold)
    }
    
    class var systemFont18: UIFont {
        return .systemFont(ofSize: 18)
    }
    
    class var systemFont24Bold: UIFont {
        return .systemFont(ofSize: 24, weight: .bold)
    }
}
